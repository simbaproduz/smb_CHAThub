let currentLanguage = "pt-BR";
let languageBound = false;

export function normalizeLanguage(value) {
  return SUPPORTED_LANGUAGES.includes(value) ? value : "pt-BR";
}

export function getCurrentLanguage() {
  return currentLanguage;
}

export function getCurrentLocale() {
  if (currentLanguage === "en") return "en-US";
  if (currentLanguage === "es") return "es-ES";
  return "pt-BR";
}

export function getLanguageLabel(language) {
  return LANGUAGE_LABELS[normalizeLanguage(language)];
}

export function t(key, params = {}, fallback = "") {
  const dict = dictionaries[currentLanguage] || dictionaries["pt-BR"];
  const ptDict = dictionaries["pt-BR"];
  const value = dict[key] ?? ptDict[key];
  if (typeof value === "function") {
    return value(params);
  }
  if (typeof value !== "string") {
    return fallback || key;
  }
  return value.replace(/\{\{\s*(\w+)\s*\}\}/g, (_, name) => String(params[name] ?? ""));
}

function readStoredLanguage() {
  try {
    return globalThis.localStorage?.getItem(STORAGE_KEY) || "";
  } catch {
    return "";
  }
}

function writeStoredLanguage(language) {
  try {
    globalThis.localStorage?.setItem(STORAGE_KEY, language);
  } catch {
    // localStorage can be unavailable in hardened browser contexts.
  }
}

function updateLanguageControls(root = document) {
  root.querySelectorAll("[data-language-option]").forEach((button) => {
    const isActive = button.dataset.languageOption === currentLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

export function applyTranslations(root = document) {
  const scope = root || document;
  document.documentElement.lang = currentLanguage;

  scope.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n, {}, element.textContent);
  });

  scope.querySelectorAll("[data-i18n-html]").forEach((element) => {
    element.innerHTML = t(element.dataset.i18nHtml, {}, element.innerHTML);
  });

  scope.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder, {}, element.getAttribute("placeholder") || ""));
  });

  scope.querySelectorAll("[data-i18n-title]").forEach((element) => {
    element.setAttribute("title", t(element.dataset.i18nTitle, {}, element.getAttribute("title") || ""));
  });

  scope.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    element.setAttribute("aria-label", t(element.dataset.i18nAriaLabel, {}, element.getAttribute("aria-label") || ""));
  });

  updateLanguageControls(scope);
}

export function initI18n(snapshot) {
  const configuredRaw = snapshot?.settings?.ui?.language;
  const storedRaw = readStoredLanguage();
  const configured = SUPPORTED_LANGUAGES.includes(configuredRaw) ? configuredRaw : "";
  const stored = SUPPORTED_LANGUAGES.includes(storedRaw) ? storedRaw : "";
  currentLanguage = stored || configured || "pt-BR";
  writeStoredLanguage(currentLanguage);
  applyTranslations();
  return currentLanguage;
}

export async function setLanguage(language, { persist = false, onChange } = {}) {
  const nextLanguage = normalizeLanguage(language);
  currentLanguage = nextLanguage;
  writeStoredLanguage(nextLanguage);
  applyTranslations();
  onChange?.(nextLanguage);
  globalThis.dispatchEvent?.(new CustomEvent("livechat:languagechange", {
    detail: { language: nextLanguage }
  }));

  if (!persist) {
    return nextLanguage;
  }

  try {
    await fetch("/api/config/ui", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ language: nextLanguage })
    });
  } catch (error) {
    console.warn("language_persist_failed", error);
  }

  return nextLanguage;
}

export function bindLanguageSwitcher({ onChange } = {}) {
  if (languageBound) {
    return;
  }
  languageBound = true;

  document.addEventListener("click", (event) => {
    const button = event.target.closest("[data-language-option]");
    if (!button) {
      return;
    }
    event.preventDefault();
    setLanguage(button.dataset.languageOption, {
      persist: true,
      onChange
    });
  });
}
