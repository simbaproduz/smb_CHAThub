function sourceWord(language, count) {
  if (language === "en") return count === 1 ? "source" : "sources";
  if (language === "es") return count === 1 ? "fuente" : "fuentes";
  return count === 1 ? "fonte" : "fontes";
}

function messageWord(language, count) {
  if (language === "en") return count === 1 ? "message" : "messages";
  if (language === "es") return count === 1 ? "mensaje" : "mensajes";
  return count === 1 ? "mensagem" : "mensagens";
}

function activeWord(language, count) {
  if (language === "en") return "active";
  if (language === "es") return count === 1 ? "activa" : "activas";
  return count === 1 ? "ativa" : "ativas";
}

